$sql[] = "INSERT INTO migration_logs (stage_name, source_key, total_count, success_count, fail_count, message)";
$sql[] = "VALUES ('pricing-etl', 'p4-pricing', NULL, NULL, NULL, " . sqlString("pricing full upsert generated from {$input}") . ");";
$sql[] = "";
$sql[] = "COMMIT;";
$sql[] = "";
$sql[] = "-- done.";

$finalSql = implode("\n", $sql) . "\n";
$ok = file_put_contents($output, $finalSql);
if ($ok === false) {
    fwrite(STDERR, "[ERROR] failed to write output file: {$output}\n");
    exit(1);
}

fwrite(STDOUT, "[OK] SQL generated: {$output}\n");
fwrite(STDOUT, "[OK] profile_id={$profileId}\n");

