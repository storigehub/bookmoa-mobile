<?php
/**
 * p4-pricing JSON -> MariaDB full upsert SQL generator
 *
 * 사용법:
 *   php docs2/generate_full_insert_from_p4_pricing.php \
 *     --input=/absolute/path/p4-pricing.json \
 *     --output=/absolute/path/pricing_full_upsert.sql \
 *     --profile-id=1
 *
 * 입력 JSON 형식:
 *   src/App.jsx 의 DEF_PRICING 구조와 동일한 객체
 */

declare(strict_types=1);

ini_set('display_errors', '1');
error_reporting(E_ALL);

function argValue(string $name, ?string $default = null): ?string
{
    global $argv;
    foreach ($argv as $a) {
        if (str_starts_with($a, "--{$name}=")) {
            return substr($a, strlen($name) + 3);
        }
    }
    return $default;
}

function sqlString(?string $v): string
{
    if ($v === null) return 'NULL';
    $escaped = str_replace("'", "''", $v);
    return "'{$escaped}'";
}

function sqlNum($v): string
{
    if ($v === null || $v === '') return 'NULL';
    if (is_bool($v)) return $v ? '1' : '0';
    if (is_numeric($v)) return (string)$v;
    return 'NULL';
}

function requireKey(array $arr, string $key): void
{
    if (!array_key_exists($key, $arr)) {
        fwrite(STDERR, "[ERROR] missing key: {$key}\n");
        exit(1);
    }
}

$input = argValue('input');
$output = argValue('output');
$profileId = (int)(argValue('profile-id', '1') ?? '1');

if (!$input || !$output) {
    fwrite(STDERR, "Usage:\n");
    fwrite(STDERR, "  php docs2/generate_full_insert_from_p4_pricing.php --input=/path/p4-pricing.json --output=/path/pricing_full_upsert.sql [--profile-id=1]\n");
    exit(1);
}

if (!is_file($input)) {
    fwrite(STDERR, "[ERROR] input file not found: {$input}\n");
    exit(1);
}

$raw = file_get_contents($input);
if ($raw === false) {
    fwrite(STDERR, "[ERROR] failed to read input file\n");
    exit(1);
}

$pricing = json_decode($raw, true);
if (!is_array($pricing)) {
    fwrite(STDERR, "[ERROR] invalid JSON input\n");
    exit(1);
}

// 필수 키 체크
foreach ([
    'printTypes',
    'printTable',
    'sideRate',
    'coverPrintRate',
    'innerPapers',
    'coverPapers',
    'coatingTable',
    'bindingTypes',
    'bindingTable',
    'endpapers',
    'postProc',
] as $k) {
    requireKey($pricing, $k);
}

$sql = [];
$sql[] = "-- Auto-generated by generate_full_insert_from_p4_pricing.php";
$sql[] = "-- Source: {$input}";
$sql[] = "-- Profile ID: {$profileId}";
