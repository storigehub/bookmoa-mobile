$sql[] = "START TRANSACTION;";
$sql[] = "";

// profile 보장
$sql[] = "INSERT INTO pricing_profiles (id, name, is_active)";
$sql[] = "VALUES ({$profileId}, '기본 가격표', 1)";
$sql[] = "ON DUPLICATE KEY UPDATE name = VALUES(name), is_active = VALUES(is_active);";
$sql[] = "";

// 기존 profile 데이터 삭제 후 재적재 (일관성 보장)
$deleteTables = [
    'pricing_print_types',
    'pricing_print_table',
    'pricing_side_rate',
    'pricing_cover_print_rate',
    'pricing_inner_papers',
    'pricing_cover_papers',
    'pricing_coating_table',
    'pricing_binding_types',
    'pricing_binding_table',
    'pricing_endpapers',
    'pricing_postproc',
];

foreach ($deleteTables as $tbl) {
    $sql[] = "DELETE FROM {$tbl} WHERE profile_id = {$profileId};";
}
$sql[] = "";

// print types
if (!empty($pricing['printTypes']) && is_array($pricing['printTypes'])) {
    $rows = [];
    foreach ($pricing['printTypes'] as $i => $pt) {
        $rows[] = "({$profileId}, " . sqlString((string)$pt) . ", " . (int)$i . ")";
    }
    $sql[] = "INSERT INTO pricing_print_types (profile_id, print_type, sort_order) VALUES";
    $sql[] = "  " . implode(",\n  ", $rows) . ";";
    $sql[] = "";
}

// print table
if (!empty($pricing['printTable']) && is_array($pricing['printTable'])) {
    $rows = [];
    foreach ($pricing['printTable'] as $r) {
        $counter = (int)($r['c'] ?? 0);
        $vals = $r['v'] ?? [];
        foreach ($pricing['printTypes'] as $idx => $pt) {
            $v = $vals[$idx] ?? null;
            $rows[] = "("
                . "{$profileId}, {$counter}, " . sqlString((string)$pt) . ", " . sqlNum($v)
                . ")";
        }
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_print_table (profile_id, counter_c, print_type, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
    }
}

// side rate
if (is_array($pricing['sideRate'])) {
    $rows = [];
    foreach ($pricing['sideRate'] as $side => $v) {
        $rows[] = "({$profileId}, " . sqlString((string)$side) . ", " . sqlNum($v) . ")";
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_side_rate (profile_id, side_name, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
    }
}

// cover print rate
if (is_array($pricing['coverPrintRate'])) {
    $rows = [];
    foreach ($pricing['coverPrintRate'] as $side => $v) {
        $rows[] = "({$profileId}, " . sqlString((string)$side) . ", " . sqlNum($v) . ")";
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_cover_print_rate (profile_id, side_name, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
    }
}

// inner papers
if (is_array($pricing['innerPapers'])) {
    $rows = [];
    foreach ($pricing['innerPapers'] as $paperName => $sizes) {
        if (!is_array($sizes)) continue;
        foreach ($sizes as $sizeCode => $v) {
            $rows[] = "("
                . "{$profileId}, " . sqlString((string)$paperName) . ", " . sqlString((string)$sizeCode) . ", " . sqlNum($v)
                . ")";
        }
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_inner_papers (profile_id, paper_name, size_code, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
    }
}

// cover papers
if (is_array($pricing['coverPapers'])) {
    $rows = [];
    foreach ($pricing['coverPapers'] as $paperName => $sizes) {
        if (!is_array($sizes)) continue;
        foreach ($sizes as $sizeCode => $v) {
            $rows[] = "("
                . "{$profileId}, " . sqlString((string)$paperName) . ", " . sqlString((string)$sizeCode) . ", " . sqlNum($v)
                . ")";
        }
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_cover_papers (profile_id, paper_name, size_code, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
    }
}

// coating table
if (is_array($pricing['coatingTable'])) {
    $rows = [];
    foreach ($pricing['coatingTable'] as $coverSize => $coatMap) {
        if (!is_array($coatMap)) continue;
        foreach ($coatMap as $coatType => $v) {
            $rows[] = "("
                . "{$profileId}, " . sqlString((string)$coverSize) . ", " . sqlString((string)$coatType) . ", " . sqlNum($v)
                . ")";
        }
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_coating_table (profile_id, cover_size, coating_type, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
    }
}

// binding types
if (!empty($pricing['bindingTypes']) && is_array($pricing['bindingTypes'])) {
    $rows = [];
    foreach ($pricing['bindingTypes'] as $i => $bt) {
        $rows[] = "({$profileId}, " . sqlString((string)$bt) . ", " . (int)$i . ")";
    }
    $sql[] = "INSERT INTO pricing_binding_types (profile_id, binding_type, sort_order) VALUES";
    $sql[] = "  " . implode(",\n  ", $rows) . ";";
    $sql[] = "";
}

// binding table
if (!empty($pricing['bindingTable']) && is_array($pricing['bindingTable'])) {
    $rows = [];
    foreach ($pricing['bindingTable'] as $r) {
        $qty = (int)($r['q'] ?? 0);
        $vals = $r['v'] ?? [];
        foreach ($pricing['bindingTypes'] as $idx => $bt) {
            $v = $vals[$idx] ?? 0;
            $rows[] = "("
                . "{$profileId}, {$qty}, " . sqlString((string)$bt) . ", " . sqlNum($v)
