}

// postproc
if (is_array($pricing['postProc'])) {
    $rows = [];
    foreach ($pricing['postProc'] as $procName => $v) {
        $rows[] = "("
            . "{$profileId}, " . sqlString((string)$procName) . ", " . sqlNum($v)
            . ")";
    }
    if ($rows) {
        $sql[] = "INSERT INTO pricing_postproc (profile_id, proc_name, unit_price) VALUES";
        $sql[] = "  " . implode(",\n  ", $rows) . ";";
        $sql[] = "";
